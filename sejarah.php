<h2>SEJARAH</h2>

<p>Pada awal berdirinya MAPA UNISI bernama Unisi Mountaneers Club (UMC). Diproklamirkan oleh 5 orang mahasiswa UII, yaitu Budi Sudjiono, Mustafa Kamal Pasha, Busra Basri, Badjoeri Doellah Joesro dan M. Hendarsin, pada tanggal 3 juli 1974 pukul 11.45 WIB. Di puncak G merbabu pada ketinggian 3.142 mdpl dengan temperatur 14C.<br><br>

